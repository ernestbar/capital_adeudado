    public void DatosContrato(int Id_contrato, bool Vigente)
    {
        terrasur.contrato c = new terrasur.contrato(Id_contrato);
        terrasur.contrato_venta cv = new terrasur.contrato_venta(Id_contrato);
        textBox8.Text = c.numero;
        //textBox40.Text = c.estado_nombre + " " + terrasur.contrato.EstadoActualFecha(Id_contrato).ToShortDateString();
        string fecha_estado = terrasur.contrato.EstadoActualFecha(Id_contrato).ToShortDateString();
        if (c.estado_nombre == "Revertido" && fecha_estado == "31/12/2011") { textBox40.Text = c.estado_nombre; }
        else { textBox40.Text = c.estado_nombre + " " + fecha_estado; }

        textBox9.Text = cv.urbanizacion_nombre_corto.ToString();
        textBox10.Text = cv.manzano_codigo.ToString();
        textBox11.Text = cv.lote_codigo.ToString();
        textBox12.Text = cv.superficie_m2.ToString("F2");
        textBox13.Text = cv.precio_m2_sus.ToString("F2");
        //textBox12.Text = new terrasur.lote(cv.id_lote).superficie_m2.ToString("F2");
        //textBox13.Text = new terrasur.lote(cv.id_lote).precio_m2_sus.ToString("F2");
        terrasur.cliente cl = new terrasur.cliente(c.id_titular);
        textBox14.Text = cl.paterno.ToString() + ' ' + cl.materno.ToString() + ' ' + cl.nombres.ToString();
        textBox24.Text = c.precio.ToString("F2");

        //decimal descuento = (c.descuento_efectivo) + ((c.descuento_porcentaje / 100) * c.precio);
        //textBox25.Text = descuento.ToString("F");
        textBox38.Text = c.descuento_porcentaje.ToString("F2");
        textBox25.Text = c.descuento_efectivo.ToString("F2");
        textBox27.Text = c.cuota_inicial.ToString("F2");
        if ((c.id_planpago_vigente > 0) && (Vigente == true))
        {
            terrasur.plan_pago pp = new terrasur.plan_pago(c.id_planpago_vigente);
            textBox28.Text = pp.num_cuotas.ToString();
            textBox29.Text = pp.cuota_base.ToString("F2");
            textBox30.Text = pp.seguro.ToString("F3");
            textBox31.Text = pp.mantenimiento_sus.ToString("F2");
            textBox32.Text = pp.interes_corriente.ToString("F2");
            textBox42.Text = pp.fecha_inicio_plan.Day.ToString();
        }
        else
        {
            textBox28.Text = c.num_cuotas.ToString();
            textBox29.Text = c.cuota_base.ToString("F2");
            textBox30.Text = c.seguro.ToString("F3");
            textBox31.Text = c.mantenimiento_sus.ToString("F2");
            textBox32.Text = c.interes_corriente.ToString("F2");
            textBox42.Text = c.fecha_inicio_plan.Day.ToString();
        }
        if (c.id_promotor_vigente > 0)
        {
            terrasur.usuario pr = new terrasur.usuario(c.id_promotor_vigente);
            textBox34.Text = pr.paterno.ToString() + ' ' + pr.materno.ToString() + ' ' + pr.nombres.ToString();
        }
        else
        {
            textBox34.Text = "Sin asignar";
        }
        if (c.id_cobrador_vigente > 0)
        {
            terrasur.usuario cb = new terrasur.usuario(c.id_cobrador_vigente);
            textBox36.Text = cb.paterno.ToString() + ' ' + cb.materno.ToString() + ' ' + cb.nombres.ToString();
        }
        else
        {
            textBox36.Text = "Sin asignar";
        }
        textBox44.Text = terrasur.cliente_contrato.ListaClientesAdicionales_String(Id_contrato);

        decimal PagadoGestor = terrasur.contrato.PagadoGestor(Id_contrato);
        if (PagadoGestor > 0)
        {
            textBox45.Visible = true;
            textBox46.Visible = true;
            textBox46.Text = PagadoGestor.ToString("F2");
        }
        else
        {
            textBox45.Visible = false;
            textBox46.Visible = false;
        }

        textBox15.Text = "Precio Total (" + c.codigo_moneda + ")";
        textBox16.Text = "Desc. (" + c.codigo_moneda + ")";
        textBox17.Text = "Precio Final (" + c.codigo_moneda + ")";
        textBox18.Text = "Cuota Inicial (" + c.codigo_moneda + ")";
        textBox20.Text = "Cuota Mensual (" + c.codigo_moneda + ")";
        textBox22.Text = "Mantenim. (" + c.codigo_moneda + "/mes)";
        textBox48.Text = new terrasur.moneda(c.id_moneda).nombre;

        int num_seguro = new terrasur.seguro_provida(Id_contrato).numero;
        if (num_seguro > 0) { textBox50.Text = num_seguro.ToString(); } else { textBox50.Text = "---"; }

        // Req. Capital Adeudado
        terrasur.capital_adeudado ca = new terrasur.capital_adeudado(Id_contrato);
        textBox53.Text = "Precio Financiado: (" + c.codigo_moneda + "):";
        textBox54.Text = c.precio_final.ToString("F2");
        textBox51.Text = "Capital Adeudado: (" + c.codigo_moneda + "):";
        textBox52.Text = ca.monto.ToString("F2");
        textBox26.Text = (c.precio_final + ca.monto).ToString("F2");
        //

    }
    private void rpt_Encabezado_ReportStart(object sender, EventArgs e)
    {
        EstilosBase rpt = new EstilosBase();
        string filepath = HttpRuntime.AppDomainAppPath + "/App_Data/EstilosBase.rpx";
        rpt.LoadLayout(filepath);
        for (int i = 4; i < rpt.StyleSheet.Count; i++)
        {
            DataDynamics.ActiveReports.Style s = rpt.StyleSheet[i];
            this.StyleSheet.Add(s.Name);
            if (s.Value != null)
            {
                this.StyleSheet[i].Value = s.Value;
            }
        }
        CargarEstilos();
    }
